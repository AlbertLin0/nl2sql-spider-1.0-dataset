
CREATE TABLE "city" (
  "city_code" varchar(3) NOT NULL,
  "city_name" varchar(25) DEFAULT NULL,
  "state" varchar(2) DEFAULT NULL,
  "country" varchar(25) DEFAULT NULL,
  "latitude" DECIMAL(10,3) DEFAULT NULL,
  "longitude" DECIMAL(10,3) DEFAULT NULL,
  PRIMARY KEY ("city_code")
) ;




CREATE TABLE "direct_distance" (
  "city1_code" varchar(3) DEFAULT NULL,
  "city2_code" varchar(3) DEFAULT NULL,
  "distance" int DEFAULT NULL,
  CONSTRAINT "direct_distance_fk_0_0" FOREIGN KEY ("city2_code") REFERENCES "city" ("city_code"),
  CONSTRAINT "direct_distance_fk_1_0" FOREIGN KEY ("city1_code") REFERENCES "city" ("city_code")
) ;




CREATE TABLE "student" (
  "stuid" INT GENERATED BY DEFAULT AS IDENTITY,
  "lname" varchar(12) DEFAULT NULL,
  "fname" varchar(12) DEFAULT NULL,
  "age" int DEFAULT NULL,
  "sex" varchar(1) DEFAULT NULL,
  "major" int DEFAULT NULL,
  "advisor" int DEFAULT NULL,
  "city_code" varchar(3) DEFAULT NULL,
  PRIMARY KEY ("stuid"),
  CONSTRAINT "student_fk_0_0" FOREIGN KEY ("city_code") REFERENCES "city" ("city_code")
) ;

